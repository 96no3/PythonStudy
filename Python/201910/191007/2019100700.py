# フォーマット文字列
"%d" % 3
"%2d" % 3
"%02d" % 3
"%06.2f" % 3.141592
"%06.4f" % 3.141592
"%0*d" % (3,4)
"%0*d" % (10,4)
"%*s" % (10,'a')
"%0*s" % (4,'a')
"%03d %3f" % (10,3.14)
"%0*d %*d" % (3,4,5,6)

# リスト内包表記
[3*i for i in range(1,10)]
"%3d |"*9 % tuple([3*i for i in range(1,10)])
[[i*j for i in range(1,10)] for j in range(1,10)]
